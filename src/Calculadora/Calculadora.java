package Calculadora;

public class Calculadora {
    public int Suma(int n1, int n2){
        return n1 + n2;
    }

    public int Resta(int n1, int n2){
        return n1 - n2;
    }

    public int Multiplicacion(int n1, int n2){
        return n1 * n2;
    }

    public float Division(int n1, int n2){
        return (float) n1 / n2;
    }

}
